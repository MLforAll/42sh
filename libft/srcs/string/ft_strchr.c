#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	size_t	i;

	i = 0;
	while (1)
	{
		if (s[i] == c)
			return ((char*)((t_uintptr)s + i));
		if (!s[i])
			break ;
		i++;
	}
	return (NULL);
}
